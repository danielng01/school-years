//Да се разработи програма, която изисква от потребителя да въведе две целочислени числа, x и y, където x < y. Да се намери и изведе сумата на числата делящи се на 17 в интервал [x, y].
#include <stdio.h>
int main()
{
	int x,y;
	scanf("%d",&x);
	scanf("%d",&y);
	int i;
	for(i=0;x<=y;x++)
	{
		if(x%17==0)
		{
			i+=x;		
		}
	}	
	printf("%d\n",i);
}
